package Clases;

public class NoGenericsMethods {
    private String a;
    private String b;
    private String c;

    public NoGenericsMethods(String a, String b, String c){
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public String getA() {
        return a;
    }

    public String getB() {
        return b;
    }

    public String getC() {
        return c;
    }

    public void setA(String a) {
        this.a = a;
    }

    public void setB(String b) {
        this.b = b;
    }

    public void setC(String c) {
        this.c = c;
    }
}
